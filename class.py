class calculator:
    
    def addition(x,y):
        added = x + y
        print('Add',added)
    def substraction(x,y):
        sub = x - y
        print('Sub',sub)    
    def multiplication(x,y):
        mul = x * y
        print('Multiplication - ',mul)
    def division(x,y):
        div = x / y
        print('Division - ',div)

calculator.multiplication(3,5)
calculator.addition(3,5)
calculator.substraction(10,8)
calculator.division(6,3)